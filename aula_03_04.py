n1 = int(input("Valor do primeiro número: "))
n2 = int(input("Valor do segundo número: "))

if (n1 == n2):
    soma= n1 * n2
    print(f"Números iguais - total da multiplicação é igual a {soma}")
else:
    soma2= n1 + n2
    print(f"Números diferentes - total da soma é igual a {soma2}")
